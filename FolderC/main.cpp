

void draw(char* vga, char color){
	*vga = color;
}

void draw_dot(char* vga, unsigned short x, unsigned short y, char color){
	y *= 320;
	vga += x;
	vga += y;

	draw(vga, color);
}

void draw_letter(unsigned short x, unsigned short y, char color, unsigned char* c){	
	char* vga = (char*)0xa0000;
	y *= 320;
	vga += x;
	vga += y;
	char dy = 0;
	char dx = 0;
	
	for(dy = 0; dy < 8; dy++){
		if(*c == 0x00){	
			// pass
		}
		else{
			for(dx = 0; dx < 8; dx++){
				if(*c & (0x80 >> dx)){
					draw_dot(vga, dx, dy, color);
				}
			} 
		}
		c++;
	}


}


void clear_screen(char* vga){//Vga vga){//unsigned long* vga){
	
	while(1){
		if(vga > (char*)0xafa00){
			return;
		}
		*vga = 1;
		//asm("inc %0;"
		//: "=r" (vga));
		vga++;
	}
	
}

